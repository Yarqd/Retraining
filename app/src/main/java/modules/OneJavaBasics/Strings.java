package modules.OneJavaBasics;

public class Strings {
    public static void strings() {
        System.out.println("строкой мы считаем все, что находится внутри кавычек, даже если это пробел, один символ или "
                + "вообще отсутствие символов.");
        System.out.println("Dragon's mother");
        System.out.println("Dragon's mother said \"No\"");
        System.out.println("Для вывода таких строк используют символ экранирования: \\");
        System.out.println("System.out.println(\"Dragon's mother said \\\"No\\\"\");");
        System.out.println("Cделать перевод строки - можно используя символ перевода строки: \\n");
        System.out.println("System.out.println() - тоже переводит строку");
        System.out.println("Конкатенация - операция соединения строк. " + "<- вот этот знак (смотри в коде)");
    }
}
